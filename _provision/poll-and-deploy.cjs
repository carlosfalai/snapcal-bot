// Wait for EC2 to be running + RDS endpoint to exist, then build the .env content for the bot.
const fs = require('fs');
const path = require('path');
const { EC2Client, DescribeInstancesCommand } = require('@aws-sdk/client-ec2');
const { RDSClient, DescribeDBInstancesCommand } = require('@aws-sdk/client-rds');

const ENV = fs.readFileSync('C:/Users/Carlos Faviel Font/.claude/.env', 'utf8');
function envVar(name) { return ENV.match(new RegExp('^' + name + '=([^\r\n]+)', 'm'))?.[1]?.trim(); }

const REGION = 'us-east-1';
const cred = {
  accessKeyId: envVar('AWS_ACCESS_KEY_ID'),
  secretAccessKey: envVar('AWS_SECRET_ACCESS_KEY'),
};
const ec2 = new EC2Client({ region: REGION, credentials: cred });
const rds = new RDSClient({ region: REGION, credentials: cred });

const STATE_FILE = path.join(__dirname, '.aws-state.json');
const state = JSON.parse(fs.readFileSync(STATE_FILE, 'utf8'));

async function pollEc2() {
  while (true) {
    const r = await ec2.send(new DescribeInstancesCommand({ InstanceIds: [state.ec2.id] }));
    const inst = r.Reservations?.[0]?.Instances?.[0];
    if (inst?.PublicIpAddress && inst.State?.Name === 'running') {
      state.ec2.public_ip = inst.PublicIpAddress;
      state.ec2.public_dns = inst.PublicDnsName;
      fs.writeFileSync(STATE_FILE, JSON.stringify(state, null, 2));
      console.log('EC2 ready:', inst.PublicIpAddress);
      return;
    }
    process.stdout.write(`.`);
    await new Promise(r => setTimeout(r, 6000));
  }
}

async function pollRds() {
  while (true) {
    try {
      const r = await rds.send(new DescribeDBInstancesCommand({ DBInstanceIdentifier: 'fotocaldb' }));
      const inst = r.DBInstances?.[0];
      if (inst?.Endpoint?.Address && inst.DBInstanceStatus === 'available') {
        state.rds.endpoint = inst.Endpoint.Address;
        state.rds.port = inst.Endpoint.Port || 5432;
        state.rds.status = inst.DBInstanceStatus;
        fs.writeFileSync(STATE_FILE, JSON.stringify(state, null, 2));
        console.log('\nRDS ready:', state.rds.endpoint);
        return;
      }
      process.stdout.write(`r(${inst?.DBInstanceStatus || '?'})`);
    } catch (e) { process.stdout.write('?'); }
    await new Promise(r => setTimeout(r, 15000));
  }
}

(async () => {
  console.log('Waiting for EC2 + RDS to be ready...');
  await Promise.all([pollEc2(), pollRds()]);

  const databaseUrl = `postgres://${state.rds.user}:${encodeURIComponent(state.rds.password)}@${state.rds.endpoint}:${state.rds.port}/${state.rds.db}`;
  console.log('\nDATABASE_URL ready');

  const env = `# Auto-generated by poll-and-deploy.cjs
TELEGRAM_BOT_TOKEN=${envVar('TELEGRAM_BOT_TOKEN_FOTOCAL') || ''}
AWS_REGION=us-east-1
AWS_ACCESS_KEY_ID=${envVar('AWS_ACCESS_KEY_ID')}
AWS_SECRET_ACCESS_KEY=${envVar('AWS_SECRET_ACCESS_KEY')}
S3_BUCKET=${state.bucket}
BEDROCK_HAIKU_MODEL=us.anthropic.claude-haiku-4-5-20251001-v1:0
DATABASE_URL=${databaseUrl}
STRIPE_SECRET_KEY=${envVar('STRIPE_SECRET_KEY')}
STRIPE_WEBHOOK_SECRET=__SET_AFTER_CADDY_HTTPS__
STRIPE_PRICE_ID=${envVar('STRIPE_PRICE_ID_FOTOCAL') || ''}
PUBLIC_BASE_URL=http://${state.ec2.public_ip}:3017
PORT=3017
ADMIN_TELEGRAM_ID=
`;
  fs.writeFileSync(path.join(__dirname, 'fotocal.env'), env);
  console.log('Wrote', path.join(__dirname, 'fotocal.env'));
})().catch(e => { console.error('poll failed:', e.message); process.exit(1); });
